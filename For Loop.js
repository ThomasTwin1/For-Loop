/*step 1. 1st and 2nd statements are checked first var i = 0; i<10; 
  step 2. if statements are true the code inside curly braces is ran
  step 3. The third statement ++i is ran
  step 4. This loop continues to happen until the 2nd statement i<10; is no longer true
  */

for (var i = 0; i<10; ++i) {
    console.log(i);
}

